/**
 * Glyphs prone to mirror-reversal in early writers (Leo's known pain point):
 * digits that read as a valid-looking shape flipped (3, 5, 7, 9) and letters
 * commonly reversed (S, Z, J, and lowercase b/d, which mirror each other).
 * `strokes.ts` biases glyph-of-the-day selection towards this set and uses
 * it to pick which glyph `espejo` quizzes on.
 */
