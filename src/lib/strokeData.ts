import type { Stroke } from '../types/exercise'

/**
 * Coarse hand-authored polyline stroke data for every glyph Leo traces:
 * uppercase A-Z + Ñ, lowercase a-z + ñ, digits 0-9. Points are percentages
 * (0-100) of a square box for easy hand-authoring; `pct` below converts
 * them to the 0..1 normalized coordinates `Stroke` actually stores.
 * Coordinates use a top-left origin (y grows downward, matching screen
 * space), which is the natural way to author "pen path" data by eye.
 *
 * Coarseness is deliberate: this is tracing guidance for a 4-5yo, not a
 * font-rendering engine, so a handful of points per stroke (rendered as
 * straight segments) is enough — the Phase 5 TracingPlayer applies a
 * generous hit-tolerance. Multi-stroke glyphs (e.g. "A", "t", "5") are
 * ordered the way a beginner is taught to write them.
 */

/** Converts a flat list of [x, y] percentage pairs (0-100) into a normalized `Stroke`. */
function pct(...points: [number, number][]): Stroke {
  return points.map(([x, y]) => ({ x: x / 100, y: y / 100 }))
}

